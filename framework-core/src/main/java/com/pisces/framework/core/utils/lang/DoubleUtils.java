    public static boolean isZero(double value) {
        return value > -PRECISION && value < PRECISION;
    }

    public static boolean isLess(double v1, double v2) {
        return (v2 - v1) > PRECISION;
    }

    public static boolean isEqual(double v1, double v2) {
        return isZero(v1 - v2);
    }

    public static boolean isLessEqual(double v1, double v2) {
        return (v2 - v1) > PRECISION || isZero(v1 - v2);
    }

    public static boolean isGreater(double v1, double v2) {
        return !isLessEqual(v1, v2);
    }

    public static boolean isGreaterEqual(double v1, double v2) {
        return !isLess(v1, v2);
    }

    /**
     * 按指定精度向上取整
     */
    public static double valueOf(double value, int number) {
        final double digits = Math.pow(10.0, number);
        return Math.ceil(value * digits) / digits;
    }

    public static double getBatchQty(double qty, double maxBatch, double minBatch, double unitBatch) {
        if (DoubleUtils.isLessEqual(qty, minBatch)) {
            qty = DoubleUtils.isZero(unitBatch) ? minBatch : ((int) Math.ceil(minBatch / unitBatch) * unitBatch);
        } else if (DoubleUtils.isLessEqual(maxBatch, qty)) {
            qty = DoubleUtils.isZero(unitBatch) ? maxBatch : ((int) Math.ceil(maxBatch / unitBatch) * unitBatch);
        } else if (!DoubleUtils.isZero(unitBatch)) {
            if (DoubleUtils.isLessEqual(qty, unitBatch)) {
                qty = unitBatch;
            } else {
                int cnt = (int) Math.ceil(qty / unitBatch);
                if (cnt * unitBatch > maxBatch) {
                    cnt--;
                }
                qty = cnt * unitBatch;
            }
        }
        return qty;
    }

