package com.pisces.framework.core.config;

/**
 * 核心信息
 *
 * @author Jason
 * @date 2022/12/07
 */
public enum CoreMessage {
    /**
     * 无效对象名称
     */
    InvalidObjectName,
    /**
     * 无效属性
     */
    InvalidProperty,
    /**
     * 不是实体或列表
     */
    NotEntityOrList,
    /**
     * 表达错误
     */
    ExpressionError,
    /**
     * 不支持操作
     */
    NotSupportOperation,
    /**
     * 文本格式错误
     */
    TextError,
    /**
     * 浮点数格式错误
     */
    DoubleError,
    /**
     * 日期时间格式错误
     */
    DateTimeError,
    /**
     * 枚举类型错误
     */
    EnumTypeError,
    /**
     * 枚举项错误
     */
    EnumItemError,
    /**
     * 等号格式错误
     */
    EqualTypeError,
    /**
     * 无效函数名
     */
    InvalidFunctionName,
    /**
     * 太多参数
     */
    TooManyParams,
    /**
     * 无效参数类型
     */
    InvalidParamType,
    /**
     * 更少参数
     */
    LessParams,
    /**
     * 周期误差
     */
    CycleError
}
